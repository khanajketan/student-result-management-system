    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rno = jTextField1.getText();
        String physics = jTextField2.getText();
        String chemistry = jTextField3.getText();
        String maths = jTextField4.getText();
        String electronics = jTextField6.getText();
        String english = jTextField7.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root","149619#Sql");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where roll_no='"+rno+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into marks(rollno,physics, chemistry,maths,electronics,english) values('"+rno+"','"+physics+"','"+chemistry+"','"+maths+"','"+electronics+"','"+english+"')");
            JOptionPane.showMessageDialog(null,"Data updated Successfully");
            }else{
                JOptionPane.showMessageDialog(null,"student is not registered");
            }
            
            setVisible(false);
            new addresult().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

